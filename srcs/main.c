#include "so_long.h"

int	main(int ac, char **av)
{
	t_game	game;

	if (!start(&game, ac, av))
		return (0);
	mlx_hook(game.window, 2, 0, input, (void *)&game);
	mlx_loop_hook(game.mlx, update, (void *)&game);
	mlx_loop(game.mlx);
	return (0);
}
